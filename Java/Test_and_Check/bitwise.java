public class bitwise {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 2;
        int num3, num4;

        num3= 7 & 2; // bitwise AND
        System.out.println(num3); // output: 2

        num4 = 7 | 2; // bitwise OR
        System.out.println(num4); // output: 7
    }
}
